        result := identity_mat4_fixed;
        cos_angle := cos_fixed(angle);
        sin_angle := sin_fixed(angle);
        result(0)(0) := cos_angle;
        result(0)(1) := - sin_angle;
        result(1)(0) := sin_angle;
        result(1)(1) := cos_angle;
        RETURN result;
    END FUNCTION;

    -- Rotation
    FUNCTION rotation_mat4_fixed (euler : vec3_int) RETURN mat4_fixed IS
        VARIABLE result : mat4_fixed;
        VARIABLE a, b, c, d, e, f, g, h, i : INTEGER;
        VARIABLE cos_x, cos_y, cos_z, sin_x, sin_y, sin_z : INTEGER;
    BEGIN
        result := identity_mat4_fixed;

        cos_x := cos_fixed(euler(0));
        cos_y := cos_fixed(euler(1));
        cos_z := cos_fixed(euler(2));
        sin_x := sin_fixed(euler(0));
        sin_y := sin_fixed(euler(1));
        sin_z := sin_fixed(euler(2));

        a := mult_fixed(cos_y, cos_z);
        b := mult_fixed(cos_y, sin_z);
        c := - sin_y;
        d := mult_fixed(mult_fixed(sin_x, sin_y), cos_z) - mult_fixed(cos_x, sin_z);
        e := mult_fixed(mult_fixed(sin_x, sin_y), sin_z) + mult_fixed(cos_x, cos_z);
        f := mult_fixed(sin_x, cos_y);
        g := mult_fixed(cos_x, mult_fixed(sin_y, cos_z)) + mult_fixed(sin_x, sin_z);
        h := mult_fixed(cos_x, mult_fixed(sin_y, sin_z)) - mult_fixed(sin_x, cos_z);
        i := mult_fixed(cos_x, cos_y);

        result(0)(0) := a;
        result(0)(1) := b;
        result(0)(2) := c;
        result(1)(0) := d;
        result(1)(1) := e;
        result(1)(2) := f;
        result(2)(0) := g;
        result(2)(1) := h;
        result(2)(2) := i;

        RETURN result;
    END FUNCTION;

    -- Scaling
    FUNCTION scaling_mat4_fixed (scale : vec3_int) RETURN mat4_fixed IS
        VARIABLE result : mat4_fixed;
    BEGIN
        result := identity_mat4_fixed;
        result(0)(0) := to_fixed(scale(0));
        result(1)(1) := to_fixed(scale(1));
        result(2)(2) := to_fixed(scale(2));
        RETURN result;
    END FUNCTION;

