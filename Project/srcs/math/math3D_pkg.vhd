    FUNCTION sin_fixed (x : INTEGER) RETURN fixed IS
    BEGIN
        IF x < 0 THEN
            RETURN -sin_table(-x MOD 360);
        ELSE
            RETURN sin_table(x MOD 360);
        END IF;
    END sin_fixed;

